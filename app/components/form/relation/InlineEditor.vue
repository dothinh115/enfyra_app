<script setup lang="ts">
const props = defineProps<{
  relationMeta: any;
  modelValue: any;
  disabled?: boolean;
  allowDelete?: boolean;
}>();
defineOptions({ inheritAttrs: false });

const emit = defineEmits(["update:modelValue"]);
const showModal = ref(false);
const selectedIds = ref<any[]>([]);
watch(
  () => props.modelValue,
  () => {
    if (
      props.relationMeta.type === "one-to-one" ||
      props.relationMeta.type === "many-to-one"
    ) {
      selectedIds.value =
        props.modelValue && props.modelValue.id ? [props.modelValue] : [];
    } else {
      selectedIds.value = Array.isArray(props.modelValue)
        ? props.modelValue.filter((item) => item && item.id)
        : [];
    }
  },
  { immediate: true }
);

function applySelection(ids: any[]) {
  let result;
  switch (props.relationMeta.type) {
    case "one-to-one":
    case "many-to-one":
      result = ids.length > 0 ? ids[0] : null;
      break;
    case "one-to-many":
    case "many-to-many":
      result = ids;
      break;
    default:
      result = ids;
  }

  emit("update:modelValue", result);
  showModal.value = false;
}

function removeId(id: any) {
  if (id === undefined || id === null) {
    console.warn("Cannot remove item with undefined/null id:", id);
    return;
  }

  if (
    props.relationMeta.type === "one-to-one" ||
    props.relationMeta.type === "many-to-one"
  ) {
    emit("update:modelValue", null);
    selectedIds.value = [];
  } else {
    const updated = selectedIds.value.filter((i) => i.id !== id);
    emit("update:modelValue", updated);
    selectedIds.value = updated;
  }
}

function shortenId(id: string | number): string {
  if (id === undefined || id === null) {
    return "Invalid ID";
  }
  const str = String(id);
  // Ngắn hơn nữa: 4 ký tự đầu + … + 3 ký tự cuối
  return str.length > 12 ? `${str.slice(0, 4)}…${str.slice(-3)}` : str;
}
</script>

<template>
  <div class="flex flex-wrap gap-2 items-center">
    <UBadge
      v-for="item in selectedIds"
      :key="item.id"
      size="lg"
      :color="item.id ? 'primary' : 'error'"
      variant="soft"
      class="flex items-center gap-1"
      :title="item.id ? String(item.id) : 'Invalid ID'"
    >
      {{ item.id ? shortenId(item.id) : "Invalid ID" }}
      <button
        @click.stop="removeId(item.id)"
        class="ml-1 text-xs hover:text-red-500 cursor-pointer"
        title="Delete"
        v-if="!props.disabled"
      >
        ✕
      </button>
    </UBadge>

    <UButton
      icon="lucide:pencil"
      size="md"
      variant="outline"
      color="secondary"
      @click="showModal = true"
      class="rounded-full"
    />
  </div>
  <Teleport to="body">
    <UDrawer
      v-model:open="showModal"
      direction="right"
      class="min-w-xl"
      :ui="{
        header:
          'border-b border-muted text-muted pb-2 flex items-center justify-between',
      }"
    >
      <template #header>
        <h2>
          {{ props.relationMeta.propertyName }}
        </h2>
        <UButton
          @click="showModal = false"
          icon="lucide:x"
          color="error"
          variant="ghost"
          size="xl"
        />
      </template>
      <template #body>
        <FormRelationSelector
          :relationMeta="relationMeta"
          :selected-ids="selectedIds"
          :multiple="
            relationMeta.type === 'many-to-many' ||
            relationMeta.type === 'one-to-many'
          "
          @apply="applySelection"
          :disabled="props.disabled"
          :allowDelete
        />
      </template>
    </UDrawer>
  </Teleport>
</template>
