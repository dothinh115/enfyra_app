<template>
  <Transition name="loading-fade" mode="out-in">
    <CommonLoadingState
      v-if="!isMounted || loading"
      title="Loading hook..."
      description="Fetching hook details"
      size="sm"
      type="form"
      context="page"
    />

    <div v-else-if="hookData?.data?.[0]" class="space-y-6">
      <!-- Header - Full width -->
      <CommonPageHeader
        :title="`Hook: ${hookData?.data?.[0]?.name || '(no name)'}`"
        title-size="lg"
        show-background
        background-gradient="from-red-500/6 via-orange-400/4 to-transparent"
        padding-y="py-6"
      >
        <template #badges>
          <!-- Hook Status Badges -->
          <div class="flex items-center gap-3">
            <UBadge color="primary" v-if="hookData?.data?.[0]?.isSystem"
              >System Hook</UBadge
            >
            <UBadge color="secondary" v-if="hookData?.data?.[0]?.isEnabled"
              >Enabled</UBadge
            >
          </div>
        </template>
      </CommonPageHeader>

      <!-- Content - Limited width -->
      <div class="max-w-[1000px] lg:max-w-[1000px] md:w-full">
        <UForm :state="form" @submit="updateHook">
          <FormEditorLazy
            ref="formEditorRef"
            v-model="form"
            v-model:errors="errors"
            v-model:has-changes="hasFormChanges"
            :table-name="'hook_definition'"
            :excluded="['isSystem']"
          />
        </UForm>
      </div>
    </div>

    <CommonEmptyState
      v-else
      title="Hook not found"
      description="The requested hook could not be loaded"
      icon="lucide:zap"
      size="sm"
    />
  </Transition>
</template>

<script setup lang="ts">
const route = useRoute();

const toast = useToast();
const tableName = "hook_definition";
const { confirm } = useConfirm();

const id = route.params.id as string;

const { isMounted } = useMounted();

// Form changes tracking via FormEditor
const hasFormChanges = ref(false);
const formEditorRef = ref();

const { validate, getIncludeFields } = useSchema(tableName);

useHeaderActionRegistry([
  {
    id: "save-hook",
    label: "Save",
    icon: "lucide:save",
    variant: "solid",
    color: "primary",
    size: "md",
    submit: updateHook,
    loading: computed(() => updateLoading.value),
    disabled: computed(() => !hasFormChanges.value),
    permission: {
      and: [
        {
          route: "/hook_definition",
          actions: ["update"],
        },
      ],
    },
  },
  {
    id: "delete-hook",
    label: "Delete",
    icon: "lucide:trash",
    variant: "solid",
    color: "error",
    size: "md",
    onClick: deleteHook,
    loading: computed(() => deleteLoading.value),
    permission: {
      and: [
        {
          route: "/hook_definition",
          actions: ["delete"],
        },
      ],
    },
  },
]);

const {
  data: hookData,
  pending: loading,
  execute: executeGetHook,
} = useApiLazy(() => `/${tableName}`, {
  query: { fields: getIncludeFields(), filter: { id: { _eq: id } } },
  errorContext: "Fetch Hook",
});

const {
  error: updateError,
  execute: executeUpdateHook,
  pending: updateLoading,
} = useApiLazy(() => `/${tableName}`, {
  method: "patch",
  errorContext: "Update Hook",
});

const {
  error: deleteError,
  execute: executeDeleteHook,
  pending: deleteLoading,
} = useApiLazy(() => `/${tableName}`, {
  method: "delete",
  errorContext: "Delete Hook",
});

const form = ref<Record<string, any>>({});

const errors = ref<Record<string, string>>({});

watch(
  hookData,
  (newData) => {
    if (newData?.data?.[0]) {
      form.value = { ...newData.data[0] };
    }
  },
  { immediate: true }
);

async function updateHook() {
  if (!form.value) return;

  const { isValid, errors: validationErrors } = validate(form.value);
  if (!isValid) {
    errors.value = validationErrors;
    toast.add({
      title: "Missing information",
      description: "Please fill in all required fields.",
      color: "error",
    });
    return;
  }

  await executeUpdateHook({ id, body: form.value });

  // Check if there was an error
  if (updateError.value) {
    return;
  }

  toast.add({
    title: "Success",
    color: "success",
    description: "Hook updated!",
  });
  errors.value = {};

  // Confirm form changes as new baseline
  formEditorRef.value?.confirmChanges();
}

async function deleteHook() {
  const ok = await confirm({
    title: "Are you sure?",
    content: "This action cannot be undone.",
  });
  if (!ok) return;

  await executeDeleteHook({ id });

  // Check if there was an error
  if (deleteError.value) {
    return;
  }

  toast.add({ title: "Hook deleted", color: "success" });
  await navigateTo("/settings/hooks");
}

onMounted(async () => {
  await executeGetHook();
});
</script>
